// Assignment Operator

let r = 5;
//the = is assigning a variable to a name

// Addition assignment
let newR = r + 1;
r += 1; 
console.log(newR);
//output: 6

// Subtraction assignment
r -= 1; // r = r - 1;, output 5 (after the above code run)

// Multiplication assignment
r *= 1; //r = r * 1; 

// Division assignment
r /= 1; //r = r / 1; 

// Remainder/Modulus assignment
r %= 1; // r = r % 1; just gives remainder, does not give entire equation

// Exponential assignment
r **= 1; // r = r **1;